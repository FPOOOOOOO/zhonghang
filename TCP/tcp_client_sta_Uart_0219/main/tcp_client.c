static void uart_rx_task(void *arg)
{
    static const char *RX_TASK_TAG = "RX_TASK";
    esp_log_level_set(RX_TASK_TAG, ESP_LOG_INFO);
    uint8_t* data = (uint8_t*) malloc(RX_BUF_SIZE+1);
    int datalen = 0;
    while (1) {
        //memset(payload,0,sizeof(payload));
        const int rxBytes = uart_read_bytes(UART_NUM_1, payload, RX_BUF_SIZE, 1000 / portTICK_RATE_MS);
        if (rxBytes > 0) {
            UARTstatus=UARTon;
            strcat((char *)payload,preUART);
            datalen=rxBytes+strlen(preUART);
            //datalen=strlen((char *)payload);
            payload[datalen] = 0;
            ESP_LOGI(RX_TASK_TAG, "Read %d bytes: '%s'", datalen, payload);
            //ESP_LOG_BUFFER_HEXDUMP(RX_TASK_TAG, data, rxBytes, ESP_LOG_INFO);
        }else{
            UARTstatus=UARToff;
        }
    }
    free(data);
}
